#include "push_swap.h"

void	ft_lstclear(t_list **lst, void (*del)(int))
{
	t_list	*next;

	if (!lst || !*lst)
		return ;
	while (*lst)
	{
		next = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = next;
	}
}
